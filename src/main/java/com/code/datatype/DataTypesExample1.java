package com.code.datatype;

public class DataTypesExample1 {

    public static void main(String[] args) {
        // assigning string to a variable and printing
        String myName = "  faknflaksnflansflknal        maskdmlaskmdlaks   ansdkjanskjdnaks 42834927394293 %&^%  ";
        System.out.println(myName);

        // assigning integer to a variable and printing
        int myVal = 2364846;
        System.out.println(myVal);

        // assigning long value to a variable and printing
        Long myVal3 = 46382467436L;
        System.out.println(myVal3);


        // assigning float value to a variable and printing
        // 2.3 6.7 876.55
        float myfloatVlue = 8.99F;
        System.out.println(myfloatVlue);


        // assigning double value to a variable and printing
        // 34.54876574
        Double myDouble = 34.54876574D;
        System.out.println(myDouble);


        // assigning boolean value to a variable and printing
        // true or folse
        boolean myBollean = true;
        boolean myBollean2 = false;
        System.out.println(myBollean);
        System.out.println(myBollean2);


        // assigning char value to a variable and printing
        // A  B C & @
        char mychar  = 'A';
        char mychar2  = '@';
        System.out.println(mychar);
        System.out.println(mychar2);

    }
}
